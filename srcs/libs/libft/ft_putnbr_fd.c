#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int long	i;
	char		c;

	i = n;
	if (i < 0)
	{
		write(fd, "-", 1);
		i = i * (-1);
	}
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
	}
	c = i % 10 + '0';
	ft_putchar_fd(c, fd);
}
