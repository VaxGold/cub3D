double angle_laps(double a)
{
    int laps;

    laps = (int)a % 360;
    return(a - 360 * laps);
}

double  rad_to_deg(double n)
{
    double  res;
    int     lap;
    
    res = n * 180 / M_PI;
    res = angle_laps(res);
    if (res < 0)
    {
        res += 360;
    }
    return (res);
}

